Suhu=int(input("masukan suhu dalam celsius = "))
kelvin= Suhu + float(273.15)
reamur= Suhu * 4 /5
fahrenheit = (Suhu * 9 / 5)+32
print('Hasil konversi dari suhu',Suhu,'derajat celsius ke kelvin adalah :' ,int(kelvin), ' K derajat')
print('Hasil konversi dari suhu',Suhu,'derajat celsius ke reamur adalah :' ,int(reamur), ' Reamur derajat')
print('Hasil konversi dari suhu',Suhu,'derajat celsius ke fahrenheit adalah :' ,int(fahrenheit), ' F derajat')

